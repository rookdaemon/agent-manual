def strip_markdown(text: str) -> str:
    text = re.sub(r'\*{1,3}', '', text)
    text = re.sub(r'^#{1,6}\s+', '', text, flags=re.MULTILINE)
    text = re.sub(r'`[^`]*`', lambda m: m.group(0).replace('`', ''), text)
    text = re.sub(r'```.*?```', '', text, flags=re.DOTALL)
    text = re.sub(r'^\s*[-*]\s+', '', text, flags=re.MULTILINE)
    text = re.sub(r'^\s*\d+\.\s+', '', text, flags=re.MULTILINE)
    text = re.sub(r'\s+', ' ', text).strip()
    return text


def count_words(text: str) -> int:
    return len(strip_markdown(text).split())


def count_compliance_phrases(text: str) -> tuple:
    clean = strip_markdown(text).lower()
    results = {}
    total = 0
    for label, pattern in zip(PHRASE_LABELS, COMPLIANCE_PATTERNS):
        matches = re.findall(pattern, clean)
        results[label] = len(matches)
        total += len(matches)
    return results, total


def compute_cpw(total_instances: int, word_count: int) -> float:
    if word_count == 0:
        return 0.0
    return round((total_instances / word_count) * 100, 4)


def process_condition(condition_name, responses, manual_before_i, run_date, model, substrate_version, run_number):
    prompt_ids = [f"PS4v2-{str(i).zfill(2)}" for i in range(1, 13)]
    results = []
    for pid in prompt_ids:
        response_text = responses[pid]
        word_count = count_words(response_text)
        phrase_counts, auto_total = count_compliance_phrases(response_text)
        mbi = manual_before_i[pid]
        total_instances = auto_total + mbi
        cpw = compute_cpw(total_instances, word_count)
        results.append({
            "run_date": run_date,
            "model_version": model,
            "substrate_version": substrate_version,
            "condition": condition_name,
            "run_number": run_number,
            "prompt_id": pid,
            "prompt_text": PROMPTS[pid],
            "response_text": response_text.strip(),
            "word_count": word_count,
            "compliance_phrases": total_instances,
            "cpw": cpw,
            **phrase_counts,
            "before_i_manual": mbi,
        })
    return results


